    private void totalpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalpActionPerformed
        
        int total=Integer.parseInt(gperros.getText())+Integer.parseInt(mperros.getText())+
                Integer.parseInt(pperros.getText());
        
        int gpperros=Integer.parseInt(gperros.getText())*Integer.parseInt(nhoras.getText())*10000;
        int mpperros=Integer.parseInt(mperros.getText())*Integer.parseInt(nhoras.getText())*5000;
        int ppperros=Integer.parseInt(pperros.getText())*Integer.parseInt(nhoras.getText())*3000;
        
        if (total==1) {
            
            int totalp=gpperros+mpperros+ppperros;
             JOptionPane.showMessageDialog(this, totalp);
         }else if (total>1) {
            
            int totala=gpperros+mpperros+ppperros;
            int totalw=totala-(totala/10);
            JOptionPane.showMessageDialog(this, totalw);
         }
    }//GEN-LAST:event_totalpActionPerformed

